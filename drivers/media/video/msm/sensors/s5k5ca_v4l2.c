static int s5k5ca_sensor_match_id(struct msm_sensor_ctrl_t  *s_ctrl)
{
	int rc = 0;
	uint16_t model_id = 0;

	rc = msm_camera_i2c_write(s_ctrl->sensor_i2c_client, 0x002E,0x0040, MSM_CAMERA_I2C_WORD_DATA);
	rc = msm_camera_i2c_read(s_ctrl->sensor_i2c_client, 0x0f12, &model_id, MSM_CAMERA_I2C_WORD_DATA);

	if ((rc < 0)||(model_id != 0x05ca)){
		CDBG("s5k5ca_read_model_id model_id(0x%04x) mismatch!\n",model_id);
		return -ENODEV;
	}
	CDBG("s5k5ca_read_model_id model_id(0x%04x) match success!\n",model_id);

	return rc;
}

int32_t s5k5ca_msm_sensor_setting(struct msm_sensor_ctrl_t *s_ctrl,
		int update_type, int res)
{
	int32_t rc = 0;
	static int isFirstPreview;

	if (update_type == MSM_SENSOR_REG_INIT) {
		CDBG("Register INIT\n");

		s_ctrl->curr_csi_params = NULL;
		msm_sensor_enable_debugfs(s_ctrl);
		msm_sensor_write_init_settings(s_ctrl);
		msleep(20);
		csi_config = 0;
		isFirstPreview = 1;
	} else if (update_type == MSM_SENSOR_UPDATE_PERIODIC) {
		CDBG("PERIODIC : %d\n", res);

		if (!csi_config) {
			s_ctrl->func_tbl->sensor_stop_stream(s_ctrl);
			msleep(100);

			s_ctrl->curr_csic_params = s_ctrl->csic_params[res];
			CDBG("CSI config in progress\n");
			v4l2_subdev_notify(&s_ctrl->sensor_v4l2_subdev,
					NOTIFY_CSIC_CFG, s_ctrl->curr_csic_params);
			CDBG("CSI config is done\n");
			mb();
			msleep(50);
			csi_config = 1;

			s_ctrl->func_tbl->sensor_start_stream(s_ctrl);
			msleep(100);
		}

		if (isFirstPreview != 1)
		{
			msm_sensor_write_conf_array(
				s_ctrl->sensor_i2c_client,
				s_ctrl->msm_sensor_reg->mode_settings, res);
		}
		if(res == 1)
			msleep(50);
		else
			msleep(300);

		isFirstPreview = 0;

		v4l2_subdev_notify(&s_ctrl->sensor_v4l2_subdev,NOTIFY_PCLK_CHANGE,
				&s_ctrl->sensordata->pdata->ioclk.vfe_clk_rate);
		msleep(50);
	}
	return rc;
}


